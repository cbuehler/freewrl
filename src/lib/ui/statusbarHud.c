int handleStatusbarHud(int mev, int* clipplane)
{
    if ((mev == ButtonPress) || (mev == ButtonRelease)) 
	{
        /* record which button is down */
		/* >>> statusbar hud */
		if( showButtons)
		{
			if(mev==ButtonPress)
				handleButtonPress();
			return 1;
		}
		if(showOptions)
		{
			if(mev==ButtonPress)
				handleOptionPress();
			return 1;
		}
	}
    if (mev == MotionNotify) 
	{
		int clipline;
		(*clipplane) = 16;
		/* >>> statusbar hud */
		if((*clipplane) || showButtons)
		{
			clipline = *clipplane;
			if(showButtons) clipline = 2*(*clipplane);
			if( screenHeight - currentY < clipline )
			{
				showButtons = 1;
				handleButtonOver();
				return 1; /* don't process for navigation */
			}
			else
			{
				showButtons = 0;
			}
		}
		if(showOptions)
		{
			/* let HUD options menu swallow button clicks */
			return 1;
		}
		/* <<< statusbar hud */
	}
	return 0;
}


